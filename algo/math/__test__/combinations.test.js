const { solution } = require("../combinations");

describe("Test combinations.js", () => {
  test("Test case 1", () => {
    expect(solution([], 3)).toEqual([]);
    expect(solution([1, 2, 3, 4], 0)).toEqual([[]]);
  });

  test("Test case 2", () => {
    expect(solution([1, 2, 3], 2)).toEqual([
      [1, 1],
      [1, 2],
      [1, 3],
      [2, 1],
      [2, 2],
      [2, 3],
      [3, 1],
      [3, 2],
      [3, 3],
    ]);

    expect(solution([1, 2, 3], 3)).toEqual([
      [1, 1, 1],
      [1, 1, 2],
      [1, 1, 3],
      [1, 2, 1],
      [1, 2, 2],
      [1, 2, 3],
      [1, 3, 1],
      [1, 3, 2],
      [1, 3, 3],
      [2, 1, 1],
      [2, 1, 2],
      [2, 1, 3],
      [2, 2, 1],
      [2, 2, 2],
      [2, 2, 3],
      [2, 3, 1],
      [2, 3, 2],
      [2, 3, 3],
      [3, 1, 1],
      [3, 1, 2],
      [3, 1, 3],
      [3, 2, 1],
      [3, 2, 2],
      [3, 2, 3],
      [3, 3, 1],
      [3, 3, 2],
      [3, 3, 3],
    ]);
  });
});
